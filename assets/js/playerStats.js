$("#monthFilter").change(function(){

    //alert('Selected value: ' + $(this).val());
    if($(this).val() === "1"){
        //Show all
        ShowTrout();
    } 
    else if($(this).val() === "2"){
        //February
        ShowRendon();
    }
    else if($(this).val() === "3"){
        //March
        ShowOhtani();
    }
});

